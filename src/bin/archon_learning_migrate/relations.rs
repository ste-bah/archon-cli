pub(crate) struct RelationSpec {
    pub(crate) name: &'static str,
    pub(crate) keys: &'static [&'static str],
    pub(crate) columns: &'static [&'static str],
}

const fn rel(
    name: &'static str,
    keys: &'static [&'static str],
    columns: &'static [&'static str],
) -> RelationSpec {
    RelationSpec {
        name,
        keys,
        columns,
    }
}

